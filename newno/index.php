    <h1><img src="newno.png" class="drawing" alt="Hand drawn icon for NEWNO - Alternative UNO Ruleset"/><p style='display: none'>NEWNO</p></h1>
    <h2>Introduction</h2>
    <i>NEWNO</i> is a custom ruleset for the popular card game <a href="https://en.wikipedia.org/wiki/Uno_(card_game)">UNO</a> that has been cobbled together over many years by a group of friends from <a href="https://en.wikipedia.org/wiki/Macclesfield">Macclesfield, England.</a>
    <h2>Gameplay</h2>
    <h3>Overview</h3>
    The basic gameplay is the same as the standard game, so read <a href="https://www.unorules.com/">those rules</a> if you aren't already familiar. The NEWNO ruleset may override original UNO rules, and the specific edge cases that may arise have been considered only in relation to the ruleset as a whole: don't expect for whatever individual house rules you have for the standard game to integrate neatly - they probably won't.<br><br>The best way to start playing NEWNO is to skim this document and begin playing, then consult when you aren't sure what's supposed to happen.<br><br>
    <hr>
    <h3>Dealing</h3>
    Deal an equal number of cards to each player, placing them face down on the table. The number of cards in each hand for a given round is left to the dealer's discretion, but all hands must contain the same number of cards at the start. Once the hands have been dealt, place the remaining deck in the middle of the table, also face down.<br><br>You cannot touch any of the hands while cards are still being dealt, but after the last card is dealt you may take any hand to be your own (not necessarily the one in front of you). Once you have touched a hand it becomes yours, as long as you were the first to touch it.<br><br>
    <hr>
    <h3>Starting to Play</h3>
    The very first move of the game is the dealer turning the top card from the face down deck over to start the <i>discard pile</i>.
    <strong>The first player to play plays first.</strong>
    Any player may start the game once the first card has been turned over, as long as their play is 'legal'. The played card should be placed face up on the top of the discard pile. See the <a href="#youngest-rule">The Youngest Rule</a> if you find the game in a stalemate during the first couple of plays.
    <h4>Special Cases</h4>
    If the turned over card is one of the below, the first player may have more leeway in what counts as a legal play:
    <div class="drawing-by-text">
      <h4><img src="plus-two.png" class="drawing" alt="Hand drawn icon for NEWNO - Plus Two Card"/><p style='display: none'>Plus Two</p></h4>
      <p>The first player doesn't have to draw any cards, they can just play any card of the same colour.
    </div>
    <hr>
    <div class="drawing-by-text">
      <h4><img src="plus-four.png" class="drawing" alt="Hand drawn icon for NEWNO - Plus Four Card"/><p style='display: none'>Plus Four</p></h4>
      The first player doesn't have to draw any cards, they can just play any card of any colour.
    </div>
    <hr>
    <div class="drawing-by-text">
      <h4><img src="skip.png" class="drawing" alt="Hand drawn icon for NEWNO - Skip Card"/><p style='display: none'>Skip</p></h4>
      Nobody is actually 'skipped', the first player must just play a card of the same colour. If the first player also plays a skip, only one player to one side is skipped.
    </div>
    <hr>
    <div class="drawing-by-text">
      <h4><img src="reverse.png" class="drawing" alt="Hand drawn icon for NEWNO - Reverse Card"/><p style='display: none'>Reverse</p></h4>
      As the direction is yet to be set, any card of the same colour may be played and nothing special happens.
    </div>
    <hr>
    <div class="drawing-by-text">
      <h4><img src="wild.png" class="drawing" alt="Hand drawn icon for NEWNO - Wild Card"/><p style='display: none'>Wild</p></h4>
      Any card of any colour may be played.
    </div>
    <hr><br>
    <strong>The second player to play goes second.</strong>
    Whichever player (to either the direct left or direct right of the starting player) is quick enough to go second, may play any legal card from their hand, also placing it face up on the discard pile. After the second player has played, the direction is set.<br><br>If you find yourself in a stalemate where neither player adjacent to the first player wants to play (for example if neither can play or the first player played a +2 or +4), see <a href="#youngest-rule">The Youngest Rule</a>.<br><br>
    <hr>
  <h3>Continuing to Play</h3>
  After the first two players have played, each player in turn (in the direction set by the first two players' order) plays one card from their deck or draws one if they cannot make a legal play. Special cards may change the direction - see below for all of the special cards and their eccentricities. If you cannot play, you must draw one card from the face down deck.
  <h4>The Apex Gambit</h4>
  If you cannot play and thus draw one card, you are allowed to immediately play that card if it is legal. This does not apply when drawing cards as a penalty, for example after +2 or +4 cards.<br><br>
  <hr>
  <h2>Special Rules</h2>
  <h3 id='youngest-rule'>The Youngest Rule</h3>
  In any case where multiple players are allowed to play, for example immediately after the dealer turns over the first card in a game, or after the first player has played a card and the direction is unset, if nobody <i>wants</i> to play you may find the game in a stalemate. In this situation, the youngest player who could play next (e.g. adjacent to the first player if the stalemate occurs due to nobody wanting to go second) must play.<br><br>
  <hr>
  <h3>Challenges</h3>
  If a player does not shout 'UNO' when they are down to one card, or touch a hand that is being passed to them (for example if a 7 or 0 has been played) that only contains one card, they are liable to be challenged. Any other player can say 'challenge' if htey spot that someone has not shouted UNO, and the challenged player must draw 2 cards. Play resumes as normal from whoever's turn it was before the challenge was made.<br><br>If a player incorrectly challenges another, <i>they</i> must draw 2 cards.<br><br>
  <hr>
  <h3>Penalty Cards</h3>
  <div class="drawing-by-text">
    <h4><img src="plus-two.png" class="drawing" alt="Hand drawn icon for NEWNO - Seven Card"/><p style='display: none'>Plus Two</p></h4>
    If the player before you plays a +2, you may play a +2 if you have one, adding to the overall number (the next player would draw 4). This can go on for any amount of +2s, and when one player doesn't have a +2 to play they must draw the total number of cards.
  </div>
  <hr>
  <div class="drawing-by-text">
    <h4><img src="plus-four.png" class="drawing" alt="Hand drawn icon for NEWNO - Seven Card"/><p style='display: none'>Plus Two</p></h4>
    <p>These work in the same was as +2s (above), but +2s and +4s <i>cannot be mixed</i> - you cannot 'protect' yourself from a +2 by playing a +4 or vice versa.</p>
  </div>
  <hr>
  <h3>Swap Cards</h3>
  <div class="drawing-by-text">
    <h4><img src="seven.png" class="drawing" alt="Hand drawn icon for NEWNO - Seven Card"/><p style='display: none'>Seven</p></h4>
    Seven cards act as 'swap with one other player' cards. When you play one, you can pick one player to swap hands with.
  </div>
  <hr>
  <div class="drawing-by-text">
    <h4><img src="zero.png" class="drawing" alt="Hand drawn icon for NEWNO - Zero Card"/><p style='display: none'>Zero</p></h4>
    Zero cards act as 'swap in the direction of play' cards. Every player passes their hand to the player on whichever side of them would play next given the current direction.
  </div>
  <hr>
  <h3>The Jump In Rule</h3>
  This is the biggest change to the original UNO rules, and is the one that makes Newno so fun:
  <strong>If you have the same colour AND number, you can play it <i>even when it isn't your turn.</i></strong>
  <ul>
    <li>You can only jump in if you have the same colour AND number (or special card)</li>
    <li>Play continues from the player who jumped in</li>
    <li>Play continues in the same direction as it was going before</li>
    <li>You can jump in on yourself but you must use one hand to draw them one after each other from your other hand - you can't put both down in one movement</li>
    <li>You can jump in with special cards too, see the individual rules below</li><br>
  </ul>
  <hr>
  <div class="drawing-by-text">
    <h4><img src="reverse.png" class="drawing" alt="Hand drawn icon for NEWNO - Reverse Card"/><p style='display: none'>Reverse</p></h4>
    <p>Jumping in with a reverse card cancels the original reverse (as <a href="https://www.youtube.com/watch?v=Uk1lU3nJsZg">two reverse</a> cards = no reverse), and play continues from the player who jumped in.</p>
  </div>
  For example:
  <ul>
    <li>6 people are playing: A, B, C, D, E, and F and the play direction is clockwise (A, B, C etc)</li>
    <li>C plays a reverse card</li>
    <li>E jumps in with a reverse card of the same colour</li>
    <li>Play resumes in the original direction, clockwise, with F playing next</li><br>
  </ul>
  <hr>
  <div class="drawing-by-text">
    <h4><img src="skip.png" class="drawing" alt="Hand drawn icon for NEWNO - Skip Card"/><p style='display: none'>Skip</p></h4>
    Jumping in with a skip will add one to the number of people to be skipped, so if you are jumping in on one skip, two people after you will be skipped.
  </div>
  For example:
  <ul>
    <li>6 people are playing: A, B, C, D, E, and F</li>
    <li>A plays a skip card</li>
    <li>C jumps in with a skip card of the same colour</li>
    <li>D and E are <i>both</i> skipped, and play resumes with F playing next</li>
  </ul>
  Skips can stack, but the number of skip jump ins in a row is limited by the number of players:<br>
  <strong>You cannot jump in with a skip if you would have had your go skipped by a previously played skip card, even if the previously played skip was a jump in.</strong><br>
  <hr>
  <div class="drawing-by-text">
    <h4><img src="wild.png" class="drawing" alt="Hand drawn icon for NEWNO - Wild Card"/><p style='display: none'>Wild</p></h4>
    For normal wild cards, the player who jumped in can choose the new colour instead of the original player, however you cannot jump in on a wild card if the first player has already called the new colour.
  </div>
  <hr>
  <div class="drawing-by-text">
    <h4 style="display: flex;"><img src="plus-four.png" class="drawing" alt="Hand drawn icon for NEWNO - Plus Four Card"/><p style='display: none'>Plus Four</p></h4>
    The new player picks the colour in the same way as for a normal Wild card, however the number adds, so for the first jump in the number would increase from draw 4 to draw 8. As with a Wild, you cannot jump in if the colour has already been called (but you can still play it to defend yourself if it's your turn). You cannot use a normal wild card (without the +4) or a +2 to defend yourself from a +4.
  </div>
  <hr>
  <div class="drawing-by-text">
    <h4><img src="plus-two.png" class="drawing" alt="Hand drawn icon for NEWNO - Plus Two Card"/><p style='display: none'>Plus Two</p></h4>
    The number of cards the next player along without a +2 to play must draw adds up normally, when jumping in play will just continue from the player who jumped.
  </div>
  <hr>
  <div class="drawing-by-text">
    <h4><img src="seven.png" class="drawing" alt="Hand drawn icon for NEWNO - Seven Card"/><p style='display: none'>Seven</p></h4>
    The first player to play a swap hand card (7) swaps with a player of their choice, and afterwards the player who jumped in can swap their hand with anyone else. Alternatively the player who jumped in can choose to swap two other players' hands and keep their own. You cannot jump in with a 7 if the first player to play one has already said which player they want to switch with.
  </div>
  <hr>
  <div class="drawing-by-text">
    <h4><img src="zero.png" class="drawing" alt="Hand drawn icon for NEWNO - Zero Card"/><p style='display: none'>Zero</p></h4>
    You can only jump in with an all swap (0) if nobody has yet touched the hand that they would be receiving by swapping. If you can and do jump in with another 0, nobody gets to look at the cards they would have got with a single swap/rotation, they just pass twice in the direction of play instead.
  </div>
  <h2>Playing Cards</h2>
  It is possible to play NEWNO with a standard set of playing cards (as opposed to UNO cards), just substitute each of the special cards with normal cards as below, and use suits instead of colours.
  <ul>
    <li>0: 0</li>
    <li>2: +2</li>
    <li>7: 7</li>
    <li>8: Skip</li>
    <li>9: Reverse</li>
    <li>King: Wild</li>
    <li>Ace: Wild +4</li>
  </ul>
